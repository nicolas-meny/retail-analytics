-- A. Vérification des valeurs manquantes

SELECT 
    COUNT(*) - COUNT(LYLTY_CARD_NBR) AS MISSING_CARD_NBR,
    COUNT(*) - COUNT(LIFESTAGE) AS MISSING_LIFESTAGE,
    COUNT(*) - COUNT(PREMIUM_CUSTOMER) AS MISSING_PREMIUM
